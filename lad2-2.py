A = int(input("โปรดป้อนคะแนนของคุณ: "))  

if A >= 80 and A  <= 100 :  
    print("เกรด A")
     
elif  A >= 70 and A  <= 79 :  
    print("เกรด B")
    
elif  A >= 60 and A  <= 69 :
    print("เกรด C")
    
elif  A >= 50 and A  <= 59 :
    print("เกรด D")

elif  A >= 0 and A  <= 49 :
    print("เกรด F")
else:
    print("กรุณากรอกข้อมูลหใม่ให้ถูกต้อง น่ะฮ๊าฟไออ้วนนนนนนน")  

